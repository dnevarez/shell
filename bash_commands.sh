# sudo - use programs as other users. Specifically, and by default, the super user. Short for 'Superuser Do'

# chown - short for change owner. Has options
#  -v   - verbose mode. Prints out file what changed.
#  -R   - operates on files and directories recursively.

# ex: sudo chown -v <user/group> <file/directory> //changes owner of file/directory to user(short name) or group specified
# ex: sudo chown -Rv <user/group> <directory> // changes ownership of all files and directories within specified directory recursively to specified user (or group)
